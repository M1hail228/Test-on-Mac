print('Задача 2: Вывести все нечетные числа от 8 до 23')

i = 8

while i <= 23:
    if i % 2 == 1:
        print(i)
    i += 1
print('Конец задачи 2')

print()


print('Задача 3: Вывести квадраты чисел от 0 до 10')

